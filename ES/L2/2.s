	AREA    RESET, DATA, READONLY
    EXPORT  __Vectors
__Vectors 
	DCD  0x100000FF     ; stack pointer value when stack is empty
    DCD  Reset_Handler  ; reset vector
    ALIGN
	AREA mycode, CODE, READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=VALUE
	MOV R5,#9
	LDR R1,[R0]
	
LOOP
	LDR R3,[R0],#4
	ADDS R1,R3,R1
	ADCS R7,#0X0
	SUBS R5,#1
	BNE LOOP
	LDR R0,=RES
	STR R1,[R0],#4
	STR R7,[R0]

STOP
	B STOP
VALUE DCD 0X00000001,0X00000002,0X00000003,0X00000004,0X00000005,0X00000006,0X00000007,0X00000008,0X00000009,0X00000001
	AREA mydata, DATA, READWRITE
RES DCD 0
	END